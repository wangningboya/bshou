package cn.wy.bs.workflow.entity;

/**
 * @Author WY
 * @Date 2019/5/23
 **/
public class User {

	private String ID;

	private String name;

	private String age;

	public String getID() {
		return ID;
	}

	public void setID(String ID) {
		this.ID = ID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}
}
